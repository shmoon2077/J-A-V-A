package com.compnay.java003_ex;

public class practice {
	public static void main (String [] args) {
		int a1=1, b1=1, c1=1, d1=1;
		System.out.println(++a1);
		System.out.println(a1);
		System.out.println(b1++);
		System.out.println(b1);
		System.out.println(--c1);
		System.out.println(c1);
		System.out.println(d1--);
		System.out.println(d1);
	}
}
